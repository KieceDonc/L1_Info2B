    private void roulementCouleur(Color receive1){
        if(num==1){
            couleur1=receive1;
            num=2;
        }else{
            couleur2=receive1;
            num=1;
            deuxCouleur=true;
        }
        melanger();
    }
    
    private void melanger(){
        if(deuxCouleur){
            int R = (couleur1.getRed()+couleur2.getRed())/2;
            int G = (couleur1.getGreen()+couleur2.getGreen())/2;
            int B = (couleur1.getBlue()+couleur2.getBlue())/2;
            centre.setBackground(new Color(R,G,B));
        }
    }
    
